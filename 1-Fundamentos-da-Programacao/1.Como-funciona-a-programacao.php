Algorítimo para trocar o pinel 

SE o peneu estiver furado:
    1 - Pegar as ferramentas.
    2 - Inserir macaco em baixo do carro.
    3 - Levantar macaco levemente.
    4 - Roscar parafusos do pneu.
    5 - Levantar o macaco até a roda sair do chão.
    6 - retirar os parafusos.
    7 - Retirar a roda.
    8 - Insere a nova roda.
    9 - Abaixa o macaco levemente.
    10 - Insere os parafusos.
    11 - Aperta os parafusos.
    12 - Retira o macaco.
    13 - Re-aperta os parafusos.
CASO CONTRÁRIO:
    Dirigir até o destino.


